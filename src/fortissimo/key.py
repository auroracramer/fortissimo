chromatic_scale = ["C","C#","D","D#","E","F","F#","G","G#","A","A#","B"]

def key_dict(key):
    scale = []
    start = chromatic_scale.index(key[0])
    full_scale = chromatic_scale[start:] + chromatic_scale[:start]
    
    scale_name = ""
    if key[-1] == "M":
        scale_name = "major"
    elif key[-1] == "m":
        scale_name = "minor"
    
    index = 0        
    for x in NAMED_SCALES[scale_name]:
        scale.append(full_scale[index])
        index = index + x
        
    return scale